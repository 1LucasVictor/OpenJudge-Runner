#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
#include <math.h>

//============================================================================
//============================================================================
void enter()
{
    int w,h,x,y,r;

    scanf("%d", &w);
    scanf("%d", &h);
    scanf("%d", &x);
    scanf("%d", &y);
    scanf("%d", &r);
    if (x+r<=w && 0<=x-r && y+r<=h && 0<=y-r)
        printf("Yes\n");
    else
        printf("No\n");
}
//============================================================================
int main(void)
{
    enter();
    return 0;
}
