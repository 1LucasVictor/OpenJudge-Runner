#include<stdio.h>

#define MIN 0
#define MAX 100

int main()
{
	int a,b,c;
	
	scanf("%d %d %d", &a, &b, &c);
	
	if((a>=MIN) && (a<=MAX) && (b>=MIN) && (b<=MAX) && (c>=MIN) && (c<=MAX))
	{
		if((a<b) && (b<c))
		{
			printf("Yes\n");
		}
		else
		{
			printf("No\n");
		}
	}
	else
	{
		/* ERROR */
		return -1;
	}
	
	return 0;
}