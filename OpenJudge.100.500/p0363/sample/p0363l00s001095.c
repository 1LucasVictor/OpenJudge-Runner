#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
#include <math.h>

//============================================================================
void sort(int *a, int *b)
{
    int c;

    if (*a<*b){
        c=*a;
        *a=*b;
        *b=c;
    }
}
//============================================================================
void enter(FILE *file)
{
    int a,b,c;

    fscanf(file, "%d", &a);
    fscanf(file, "%d", &b);
    fscanf(file, "%d", &c);
    sort(&a, &b);
    sort(&b, &c);
    sort(&a, &b);
    printf("%d %d %d\n", c, b, a);
}
//============================================================================
int main(void)
{
    enter(stdin);
    return 0;
}
