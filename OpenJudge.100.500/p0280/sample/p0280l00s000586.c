#include<stdio.h>
#include<limits.h>

#define S 10000
int graph[S][S];
int n;
int cost[S];

void init(){
	int i, j;
	scanf("%d", &n);
	for( i = 0; i < n; i++ ){
		cost[i] = INT_MAX;
		for( j = 0; j < n; j++ ){
			scanf("%d", &graph[i][j]);
		}
	}
	return;
}

int update(){
	int i, j, isupdate = 0;
	for( i = 0; i < n; i++ ){
		for( j = 0; j < n; j++ ){
			if( graph[i][j] >= 0 && cost[i]+graph[i][j] < cost[j] ){
				cost[j] = cost[i]+graph[i][j];
				isupdate = 1;
			}
		}
	}
	return isupdate;
}

void get(){
	int i, j;
	for( j = 0; j < n; j++ ){
		for( i = 0; i < n; i++ ){
			if( graph[i][j] >= 0 && graph[i][j] < cost[j] ){
				cost[j] = graph[i][j];
			}
		}
	}
	return;
}

void printCost(){
	int i, sum = 0;
	for( i = 0; i < n; i++ ){ sum += cost[i]; }
	printf("%d\n", sum);
	return;
}

int main(){
	init();
	cost[0] = 0;
	//while( update() );
	get();
	printCost();
	return 0;
}