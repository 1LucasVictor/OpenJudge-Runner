    scanf("%c",&a[k-1]);
    n++;
    if(a[k-1]=='\n') break;
    if(0x41<=a[k-1]&&a[k-1]<=0x5a){ //???????????????????¶?????????????????????§??????????????¬
      a[k-1] += 0x20;
    }
