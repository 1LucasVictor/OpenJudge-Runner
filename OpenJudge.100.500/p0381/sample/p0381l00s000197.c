/* ????????????????????°?????????*/
short get_combination_cnt(short n, short x)
{
	short ii, jj, kk, result;
	
	// 1???????????°
	for( ii = 1; ii <= n; ii++ )
	{
		// 2???????????°
		for( jj = ii + 1; jj <= n; jj++ )
		{
			//3???????????°
			for( kk = jj + 1; kk <= n; kk++ )
			{
				// X??¨3????????°???????¨?????????????
				if( x == (ii + jj + kk) )
				{
					result++;
				}
			}
		}
	}
	return result;
}