#include<stdio.h>
#define _USE_MATH_DEFINES
#include<math.h>
#include <string.h>
#include<limits.h>
int main(void){
	int x, y, z, i;
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	scanf("%d%d", &x, &y);
	while (x != 0 && y != 0) {
		for (i = 0; i < x; i++) {
			printf("#");
			for (z = 0; z < y-2; z++) {
				if (i == 0 || i == x-1) {
					printf("#");
				}
				else {
					printf(".");
				}
			}
			printf("#\n");
		}
		printf("\n");
		scanf("%d%d", &x, &y);
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	return 0;
}
