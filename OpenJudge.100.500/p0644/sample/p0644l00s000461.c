#include <stdio.h>

int main(){


        int g;
        scanf("%d",&g);
        switch (g){
                case 000: printf("0\n"); break;
                case 001: printf("1\n"); break;
                case 010: printf("1\n"); break;
                case 011: printf("2\n"); break;
                case 100: printf("1\n"); break;
                case 101: printf("2\n"); break;
                case 110: printf("2\n"); break;
                case 111: printf("3\n"); break;

        }


        return 0;

}