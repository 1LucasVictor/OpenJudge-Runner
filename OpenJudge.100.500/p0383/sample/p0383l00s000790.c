short get_maltiplication(short n, short m, short l, short mat1[n][m],
	short mat2[m][l], short current_n, short current_l);

int main(void)
{
	// ??????????¬????
	short n = 0, m = 0, l = 0;
	
	// ?¬?????????????
	scanf("%hd %hd %hd", &n, &m, &l);
	
	short matrix1[n][m];
	short matrix2[m][l];
	
	short ii, jj;
	
	// 1????????????????????\????????????
	for( ii = 0; ii < n; ii++ )
	{
		for( jj = 0; jj < m; jj++)
		{
			matrix1[ii][jj] = 0;
			scanf("%hd", &matrix1[ii][jj]);
		}
	}
	
	// 2????????????????????\????????????
	for( ii = 0; ii < m; ii++ )
	{
		for( jj = 0; jj < l; jj++ )
		{
			matrix2[ii][jj] = 0;
			scanf("%hd", &matrix2[ii][jj]);
		}
	}
	
	// ??????????´???¨???????????????
	short ans_matrix[n][l];
	for( ii = 0; ii < n; ii++ )
	{
		for( jj = 0; jj < l; jj++)
		{
			ans_matrix[ii][jj] = get_maltiplication(n, m, l, matrix1, matrix2,
				ii, jj);
		}
	}
	
	// ???????????????
	for( ii = 0; ii < n; ii++ )
	{
		for( jj = 0; jj < l; jj++ )
		{
			printf("%hd", ans_matrix[ii][jj]);
			if( jj < l - 1 )
			{
				printf(" ");
			}
			else
			{
				printf("\n");
			}
		}
	}
	
	return 0;
}

/* ????????????????????¢??° */
short get_maltiplication(short n, short m, short l, short mat1[n][m],
	short mat2[m][l], short current_n, short current_l)
{
	short ret_multi = 0;
	short k = 0;
	for( k = 0; k < m; k++ )
	{
		ret_multi += mat1[current_n][k] * mat2[k][current_l];
	}
	
	return ret_multi;
}