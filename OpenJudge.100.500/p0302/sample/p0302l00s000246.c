#include <stdio.h>
#include <string.h>

#define DEF_ELEM_MAX 1000001

#define M DEF_ELEM_MAX
#define L 14

char H[M][L];

/////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
int h1(int key)
{
	return (key%M);
}

/////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
int h2(int key)
{
	return (1 + (key%(M-1)));
}

int h(int key, int i)
{
	return ((h1(key)+i*h2(key))%M);
}

/////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
int Char2No(char ch)
{
	int ret;
	switch (ch) {
	case 'A':
		ret = 1;
		break;
	case 'C':
		ret = 0x10;
		break;
	case 'G':
		ret = 0x100;
		break;
	case 'T':
		ret = 0x1000;
		break;
	}
	return ret;
}
/////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
int getkey(char *str)
{
	int i;
	int key = 0;
	for ( i = 0 ; i < (int)strlen(str) ; i++ ) {
		key += Char2No(str[i]);
	}
	return key;
}
/////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
int insert(char *str)
{
	int key = getkey(str);
	int j;
	int i = 0;
	for ( i = 0 ; ;i++ ) {
		j = h(key, i);
		if (strlen(H[j]) == 0) {
			strcpy(H[j], str);
			return 0;
		} else if (memcmp(H[j], str, strlen(str)+1) == 0) {
			return 1;
		}
	}
}

/////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////
int find(char *str)
{
	int j;
	int key = getkey(str);
	int i = 0;
	for ( i = 0 ; ;i++ ) {
		j = h(key, i);
		if (memcmp(H[j], str, strlen(str)+1) == 0) {
			return 1;
		} else if (strlen(H[j]) == 0) {
			return 0;
		}
	}
}

/////////////////////////////////////////////////////////////////////////////////
