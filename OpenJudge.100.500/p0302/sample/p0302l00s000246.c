/////////////////////////////////////////////////////////////////////////////////
int main(void)
{
	int i;
	char cmd[16] = {0};
	char str[14] = {0};
	char *pMsg;
	int N;

//
	scanf("%d", &N);
	for ( i = 0 ; i < N ; i++ ) {
		scanf("%s %s", cmd, str);
		if ( cmd[0] == 'i') {
			insert(str);
		} else {
			if (find(str)) {
				pMsg = "yes";
			} else {
				pMsg = "no";
			}
			printf("%s\n", pMsg);
		}
	}
	return 0;
}