#include <stdio.h>
int main(void)
{
	int a,b,c,d,e,f;
	double x=0,y=0;

	while (scanf("%d %d %d %d %d %d", &a, &b, &c, &d, &e, &f) != EOF)
	{
		/* ?????????
			ax + by = c ...???1
			dx + ey = f ...???2

			???2????????????(a/d)?????????????????????????????????????????¨y???????????????
			ax + e(a/d)y = f(a/d) ...???2'

			(b - ea/d) y = (c - fa/d) ...???1 - ???2'


			???2????????????(b/e)?????????????????????????????????????????¨x???????????????
			d(b/e)x + by = f(b/e) ...???2''

			(a - db/e) x = (c - fb/e) ...???1 - ???2''
		*/

		x = (double) (c*e - b*f) / (a*e - b*d);
		y = (double) (c*d - a*f) / (b*d - a*e);

		/* -0????????????????????? */

		if (x == -0)
		{
			x = 0;
		}

		if (y == -0)
		{
			y = 0;
		}

		printf("%.3lf %.3lf\n", x, y);
	}
	return 0;
}