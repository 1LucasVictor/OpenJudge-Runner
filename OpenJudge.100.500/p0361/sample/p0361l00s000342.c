#include<stdio.h>

#define MIN 0
#define MAX 86400

int main()
{
	int S,h,m,ss;
	
	scanf("%d", &S);
	
	/* ????????¶?´? */
	if((S>=MIN) && (S<MAX))
	{
		h=S/3600;
		S-=h*3600;
		m=S/60;
		ss=S-m*60;
		
		printf("%d:%d:%d\n", h, m, ss);
	}
	else
	{
		/* ERROR */
		return -1;
	}
	
	return 0;
}