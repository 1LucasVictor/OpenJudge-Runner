	int x, y, z,i,ans = 0;
	char op;
	char p[5][2] = { "+","-","*","/" ,"?"};

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	scanf("%d %c %d",&x, &op,&y);
	while(op != p[4][0]){
		if (op == p[0][0]) {
			//("plus");
			printf("%d\n", x + y);
		}
		else if (op == p[1][0]) {
			printf("%d\n", x - y);
		}
		else if (op == p[2][0]) {
			printf("%d\n", x * y);
		}
		else if (op == p[3][0]) {
			printf("%d\n", x / y);
		}
		scanf("%d %c %d", &x, &op, &y);
	}


	
