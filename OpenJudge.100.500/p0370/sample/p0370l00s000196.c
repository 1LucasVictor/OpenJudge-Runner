#include<stdio.h>

#define MAX 20000
#define MIN 0

int main()
{
	int a,b;
	char c,enter;
	
	while( scanf("%d %c %d%c", &a, &c, &b, &enter)==4 )
	{
		if((a>=MIN) && (a<=MAX) && (b>=MIN) && (b<=MAX))
		{
			if(c=='+') printf("%d\n", a+b);
			else if(c=='-') printf("%d\n", a-b);
			else if(c=='*') printf("%d\n", a*b);
			else if((c=='/') && (b!=0)) printf("%d\n", a/b);
			else if(c=='?') break;
			else return -1;
		}
		else
		{
			/* ERROR */
			return -1;
		}
	}
	
	return 0;
}